"""Для данного числа n<100 закончите фразу “На лугу пасется...” одним из возможных продолжений: “n коров”,
“n корова”, “n коровы”, правильно склоняя слово “корова”."""

n = int(input())
if n < 100 and (n % 10 == 1 and n != 11):
    print(str(n) + " korova")
elif n < 100 and ((n % 10 in [2, 3, 4]) and (n not in [11, 12, 13, 14])):
    print(str(n) + " korovy")
elif n < 100 and ((n % 10 == 0) or (n % 10 >= 5) or n in [11, 12, 13, 14]):
    print(str(n) + " korov")
